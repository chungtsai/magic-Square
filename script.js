    function updateCellContent(cell, idx) {
        cell.innerHTML = '';
        const val = board[idx];
        
        if (val !== 0) {
            const tile = document.createElement('div');
            tile.className = `tile-num ${clues[idx] ? 'fixed' : ''}`;
            tile.innerText = val;
            
            // Draggable setups
            if (!clues[idx]) {
                tile.setAttribute('draggable', 'true');
                
                // Drag start
                tile.addEventListener('dragstart', (e) => {
                    initAudio();
                    e.dataTransfer.setData('text/plain', JSON.stringify({ source: 'grid', index: idx, value: val }));
                    e.dataTransfer.effectAllowed = 'move';
                    cell.classList.add('selected');
                    selectedGridCellIndex = idx;
                    playSound('click');
                });
                
                tile.addEventListener('dragend', () => {
                    cell.classList.remove('selected');
                    selectedGridCellIndex = null;
                });
            }
            
            cell.appendChild(tile);
        }
    }
    
    function renderNumberBank() {
        if (gameMode === 'swap') {
            numberBankWrapper.classList.add('hidden');
            return;
        }
        numberBankWrapper.classList.remove('hidden');
        numberBank.innerHTML = '';
        
        const maxNum = gridSize * gridSize;
        
        // Find which numbers are already used
        const usedNumbers = new Set(board.filter(x => x !== 0));
        
        for (let i = 1; i <= maxNum; i++) {
            const tile = document.createElement('div');
            tile.className = 'bank-tile';
            tile.dataset.number = i;
            tile.innerText = i;
            
            if (usedNumbers.has(i)) {
                tile.classList.add('used');
            } else {
                tile.setAttribute('draggable', 'true');
                
                // Highlight selection on click
                tile.addEventListener('click', (e) => {
                    e.stopPropagation();
                    initAudio();
                    if (selectedBankNum === i) {
                        selectedBankNum = null;
                        tile.classList.remove('selected');
                        playSound('click');
                    } else {
                        // Deselect other
                        document.querySelectorAll('.bank-tile').forEach(t => t.classList.remove('selected'));
                        selectedBankNum = i;
                        tile.classList.add('selected');
                        playSound('click');
                    }
                });
                
                // Drag start events
                tile.addEventListener('dragstart', (e) => {
                    initAudio();
                    e.dataTransfer.setData('text/plain', JSON.stringify({ source: 'bank', value: i }));
                    e.dataTransfer.effectAllowed = 'copy';
                    
                    document.querySelectorAll('.bank-tile').forEach(t => t.classList.remove('selected'));
                    selectedBankNum = i;
                    tile.classList.add('selected');
                    playSound('click');
                });
                
                tile.addEventListener('dragend', () => {
                    tile.classList.remove('selected');
                    selectedBankNum = null;
                });
            }
            numberBank.appendChild(tile);
        }
    }

    /* ==========================================================================
       Grid Cell Interactivity (Events)
       ========================================================================== */
    function setupCellEvents(cell, idx) {
        // Drag Over
        cell.addEventListener('dragover', (e) => {
            if (clues[idx] || !isGameActive) return;
            e.preventDefault(); // necessary to allow drop
            cell.classList.add('selected');
        });
        
        // Drag Leave
        cell.addEventListener('dragleave', () => {
            cell.classList.remove('selected');
        });
        
        // Drop
        cell.addEventListener('drop', (e) => {
            if (clues[idx] || !isGameActive) return;
            e.preventDefault();
            cell.classList.remove('selected');
            
            try {
                const data = JSON.parse(e.dataTransfer.getData('text/plain'));
                if (data.source === 'bank') {
                    handlePlaceNum(idx, data.value);
                } else if (data.source === 'grid') {
                    handleMoveNum(data.index, idx);
                }
            } catch (err) {
                console.error("Drop error", err);
            }
        });
        
        // Mouse click placement / swap logic
        cell.addEventListener('click', (e) => {
            e.stopPropagation();
            if (!isGameActive) return;
            initAudio();
            
            if (clues[idx]) {
                playSound('error');
                showToast('這是關卡預設的數字，無法修改！', 'error');
                return;
            }
            
            if (gameMode === 'fill') {
                if (selectedBankNum !== null) {
                    // Place selected bank number here
                    handlePlaceNum(idx, selectedBankNum);
                    selectedBankNum = null;
                    document.querySelectorAll('.bank-tile').forEach(t => t.classList.remove('selected'));
                } else {
                    // Click on a cell with no bank tile selected: select this cell or clear it
                    if (board[idx] !== 0) {
                        // Double click clears, single click highlights/swaps?
                        // Let's toggle highlight for swap or just clear it on click if double-clicked
                        if (selectedGridCellIndex === null) {
                            selectedGridCellIndex = idx;
                            cell.classList.add('swap-source');
                            playSound('click');
                        } else if (selectedGridCellIndex === idx) {
                            selectedGridCellIndex = null;
                            cell.classList.remove('swap-source');
                            playSound('click');
                        } else {
                            // Swap internal grid cells
                            handleMoveNum(selectedGridCellIndex, idx);
                            document.querySelectorAll('.grid-cell').forEach(c => c.classList.remove('swap-source'));
                            selectedGridCellIndex = null;
                        }
                    }
                }
            } else if (gameMode === 'swap') {
                if (selectedGridCellIndex === null) {
                    // First click selects swap source
                    selectedGridCellIndex = idx;
                    cell.classList.add('swap-source');
                    playSound('click');
                } else {
                    // Second click swaps
                    if (selectedGridCellIndex === idx) {
                        // click same cell - cancel
                        selectedGridCellIndex = null;
                        cell.classList.remove('swap-source');
                        playSound('click');
                    } else {
                        handleMoveNum(selectedGridCellIndex, idx);
                        document.querySelectorAll('.grid-cell').forEach(c => c.classList.remove('swap-source'));
                        selectedGridCellIndex = null;
                    }
                }
            }
        });
        
        // Double click clears number in Fill Mode
        cell.addEventListener('dblclick', () => {
            if (!isGameActive || gameMode !== 'fill' || clues[idx]) return;
            if (board[idx] !== 0) {
                const clearedVal = board[idx];
                board[idx] = 0;
                moveCount++;
                updateStatsUI();
                updateCellContent(cell, idx);
                renderNumberBank();
                calculateAndSetSums();
                playSound('clear');
                showToast(`已清除數字 ${clearedVal}`, 'info');
            }
        });
    }

