    // Siamese Method for Odd Dimension Magic Squares (3x3 and 5x5)
    function generateOddMagicSquare(n) {
        const grid = Array(n).fill(null).map(() => Array(n).fill(0));
        let r = 0;
        let c = Math.floor(n / 2);
        
        for (let v = 1; v <= n * n; v++) {
            grid[r][c] = v;
            
            let nr = r - 1;
            let nc = c + 1;
            
            if (nr < 0 && nc >= n) {
                nr = r + 1;
                nc = c;
            } else {
                if (nr < 0) nr = n - 1;
                if (nc >= n) nc = 0;
            }
            
            if (grid[nr][nc] !== 0) {
                nr = r + 1;
                nc = c;
            }
            
            r = nr;
            c = nc;
        }
        
        // Flatten 2D to 1D
        return grid.flat();
    }
    
    // Doubly-Even Method for 4x4 Magic Square
    function generate4x4MagicSquare() {
        const arr = [];
        // Fill 1 to 16
        for (let i = 1; i <= 16; i++) {
            arr.push(i);
        }
        
        // Diagonals swap
        for (let i = 0; i < 16; i++) {
            const r = Math.floor(i / 4);
            const c = i % 4;
            
            // Check if element is on diagonals of the 4x4 grid
            if (r === c || r + c === 3) {
                arr[i] = 17 - arr[i];
            }
        }
        return arr;
    }
    
    // Scramble / Rotate / Reflect a Magic Square to introduce variety
    function transformMagicSquare(arr, n) {
        let grid = [];
        for (let i = 0; i < n; i++) {
            grid.push(arr.slice(i * n, (i + 1) * n));
        }
        
        // Helper: Rotate 90 deg clockwise
        const rotate90 = (matrix) => {
            const size = matrix.length;
            const res = Array(size).fill(null).map(() => Array(size).fill(0));
            for (let r = 0; r < size; r++) {
                for (let c = 0; c < size; c++) {
                    res[c][size - 1 - r] = matrix[r][c];
                }
            }
            return res;
        };
        
        // Helper: Horizontal reflect
        const reflectH = (matrix) => {
            return matrix.map(row => [...row].reverse());
        };

        // Random transformations
        const rotations = Math.floor(Math.random() * 4);
        const flip = Math.random() > 0.5;
        const invertNumbers = Math.random() > 0.5; // replace x with (N^2 + 1 - x)
        
        for (let i = 0; i < rotations; i++) {
            grid = rotate90(grid);
        }
        if (flip) {
            grid = reflectH(grid);
        }
        
        let flat = grid.flat();
        if (invertNumbers) {
            const maxVal = n * n + 1;
            flat = flat.map(x => maxVal - x);
        }
        
        return flat;
    }
    
    // Generate valid solution based on gridSize
    function generateSolution(n) {
        let baseSq;
        if (n === 3 || n === 5) {
            baseSq = generateOddMagicSquare(n);
        } else if (n === 4) {
            baseSq = generate4x4MagicSquare();
        }
        
        // Transform base square to get random orientation
        return transformMagicSquare(baseSq, n);
    }
    
    // Get Magic Constant
    function getMagicConstant(n) {
        return (n * (n * n + 1)) / 2;
    }

    /* ==========================================================================
       Game Initialization
       ========================================================================== */
    function initGame() {
        // Stop timer
        clearInterval(timerInterval);
        
        // Hide modals
        modalVictory.classList.add('hidden');
        if (confetti) confetti.stop();
        
        // Generate solution
        solution = generateSolution(gridSize);
        
        // Initialize state arrays
        const totalCells = gridSize * gridSize;
        board = Array(totalCells).fill(0);
        clues = Array(totalCells).fill(false);
        
        moveCount = 0;
        timerSeconds = 0;
        selectedBankNum = null;
        selectedGridCellIndex = null;
        isGameActive = true;
        
        updateStatsUI();
        
        // Create pre-filled clues depending on mode and difficulty
        if (gameMode === 'fill') {
            let clueCount = 0;
            if (difficulty === 'easy') {
                clueCount = Math.floor(totalCells * 0.5);
            } else if (difficulty === 'medium') {
                clueCount = Math.floor(totalCells * 0.3);
            }
            
            // Randomly select cell indices to lock as clues
            const indices = Array.from({ length: totalCells }, (_, i) => i);
            shuffleArray(indices);
            
            for (let i = 0; i < clueCount; i++) {
                const idx = indices[i];
                board[idx] = solution[idx];
                clues[idx] = true;
            }
        } else if (gameMode === 'swap') {
            // In Swap mode, grid is completely filled but scrambled
            board = [...solution];
            
            // Scramble cells that are not fixed clues
            // Note: in Swap mode, difficulty determines how many random swaps we perform
            let swapCount = 8;
            if (difficulty === 'medium') swapCount = 15;
            if (difficulty === 'hard') swapCount = 25;
            
            // Do random swaps to scramble
            for (let i = 0; i < swapCount; i++) {
                const idx1 = Math.floor(Math.random() * totalCells);
                const idx2 = Math.floor(Math.random() * totalCells);
                if (idx1 !== idx2) {
                    const temp = board[idx1];
                    board[idx1] = board[idx2];
                    board[idx2] = temp;
                }
            }
            
            // Optional: in Swap mode, easy difficulty could have a few fixed "anchor" cells
            if (difficulty === 'easy') {
                const anchorCount = Math.floor(totalCells * 0.25);
                const indices = Array.from({ length: totalCells }, (_, i) => i);
                shuffleArray(indices);
                for (let i = 0; i < anchorCount; i++) {
                    const idx = indices[i];
                    board[idx] = solution[idx];
                    clues[idx] = true; // locks this cell in place
                }
            }
        }
        
        // Set dynamic layout properties
        boardOuterWrapper.className = `board-outer-wrapper grid-${gridSize}x3`;
        
        // Render
        renderBoardGrid();
        renderNumberBank();
        calculateAndSetSums();
        
        // Start Timer
        startTimer();
    }
    
