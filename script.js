    // Hint System
    btnHint.addEventListener('click', () => {
        if (!isGameActive) return;
        initAudio();
        
        // Find indices that are not locked clues AND are either empty or incorrect
        const potentialIndices = [];
        for (let i = 0; i < board.length; i++) {
            if (!clues[i] && (board[i] === 0 || board[i] !== solution[i])) {
                potentialIndices.push(i);
            }
        }
        
        if (potentialIndices.length === 0) {
            showToast('所有格子的數字都已經是正確位置了！', 'info');
            return;
        }
        
        // Pick one index at random
        const targetIdx = potentialIndices[Math.floor(Math.random() * potentialIndices.length)];
        const correctVal = solution[targetIdx];
        
        // Inject hint into grid
        if (gameMode === 'fill') {
            // Find where this correct value currently is in the board (if anywhere) and clear it
            const currentIdxOfVal = board.indexOf(correctVal);
            if (currentIdxOfVal !== -1) {
                board[currentIdxOfVal] = 0;
                const cellPrev = document.querySelector(`.grid-cell[data-index="${currentIdxOfVal}"]`);
                updateCellContent(cellPrev, currentIdxOfVal);
            }
            
            board[targetIdx] = correctVal;
        } else if (gameMode === 'swap') {
            // In swap mode, the correct value MUST exist somewhere in the grid
            const currentIdxOfVal = board.indexOf(correctVal);
            if (currentIdxOfVal !== -1) {
                // Swap targetIdx content and currentIdxOfVal content
                const temp = board[targetIdx];
                board[targetIdx] = board[currentIdxOfVal];
                board[currentIdxOfVal] = temp;
                
                const cellPrev = document.querySelector(`.grid-cell[data-index="${currentIdxOfVal}"]`);
                updateCellContent(cellPrev, currentIdxOfVal);
            }
        }
        
        moveCount += 2; // Hints penalize move counts
        updateStatsUI();
        
        const cell = document.querySelector(`.grid-cell[data-index="${targetIdx}"]`);
        updateCellContent(cell, targetIdx);
        
        // Animate hint cell
        const tile = cell.querySelector('.tile-num');
        if (tile) {
            tile.classList.add('placed-anim');
            tile.style.boxShadow = '0 0 15px #ffb703';
            tile.style.borderColor = '#ffb703';
        }
        
        renderNumberBank();
        calculateAndSetSums();
        playSound('hint');
        showToast('已為你填入一個正確數字！(步數計為 +2)', 'info');
        
        checkWinCondition();
    });
    
