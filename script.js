    function playSound(type) {
        if (!isAudioEnabled) return;
        initAudio();
        
        try {
            if (audioCtx.state === 'suspended') {
                audioCtx.resume();
            }
            
            const osc = audioCtx.createOscillator();
            const gain = audioCtx.createGain();
            osc.connect(gain);
            gain.connect(audioCtx.destination);
            
            const now = audioCtx.currentTime;
            
            switch (type) {
                case 'click':
                    osc.type = 'sine';
                    osc.frequency.setValueAtTime(500, now);
                    osc.frequency.exponentialRampToValueAtTime(150, now + 0.08);
                    gain.gain.setValueAtTime(0.08, now);
                    gain.gain.linearRampToValueAtTime(0, now + 0.08);
                    osc.start(now);
                    osc.stop(now + 0.08);
                    break;
                    
                case 'place':
                    osc.type = 'sine';
                    osc.frequency.setValueAtTime(650, now);
                    osc.frequency.exponentialRampToValueAtTime(350, now + 0.12);
                    gain.gain.setValueAtTime(0.12, now);
                    gain.gain.linearRampToValueAtTime(0, now + 0.12);
                    osc.start(now);
                    osc.stop(now + 0.12);
                    break;
                    
                case 'clear':
                    osc.type = 'sine';
                    osc.frequency.setValueAtTime(250, now);
                    osc.frequency.exponentialRampToValueAtTime(80, now + 0.12);
                    gain.gain.setValueAtTime(0.1, now);
                    gain.gain.linearRampToValueAtTime(0, now + 0.12);
                    osc.start(now);
                    osc.stop(now + 0.12);
                    break;
                    
                case 'swap':
                    osc.type = 'triangle';
                    osc.frequency.setValueAtTime(380, now);
                    osc.frequency.linearRampToValueAtTime(520, now + 0.08);
                    osc.frequency.linearRampToValueAtTime(280, now + 0.18);
                    gain.gain.setValueAtTime(0.1, now);
                    gain.gain.linearRampToValueAtTime(0, now + 0.18);
                    osc.start(now);
                    osc.stop(now + 0.18);
                    break;
                    
                case 'victory':
                    const notes = [261.63, 329.63, 392.00, 523.25, 659.25, 783.99, 1046.50]; // C Major chord arpeggio
                    notes.forEach((freq, idx) => {
                        const noteOsc = audioCtx.createOscillator();
                        const noteGain = audioCtx.createGain();
                        noteOsc.connect(noteGain);
                        noteGain.connect(audioCtx.destination);
                        
                        noteOsc.type = 'triangle';
                        noteOsc.frequency.setValueAtTime(freq, now + idx * 0.08);
                        
                        noteGain.gain.setValueAtTime(0, now);
                        noteGain.gain.linearRampToValueAtTime(0.12, now + idx * 0.08 + 0.02);
                        noteGain.gain.exponentialRampToValueAtTime(0.001, now + idx * 0.08 + 0.5);
                        
                        noteOsc.start(now + idx * 0.08);
                        noteOsc.stop(now + idx * 0.08 + 0.5);
                    });
                    break;
                    
                case 'hint':
                    osc.type = 'sine';
                    osc.frequency.setValueAtTime(440, now);
                    osc.frequency.linearRampToValueAtTime(880, now + 0.2);
                    gain.gain.setValueAtTime(0.08, now);
                    gain.gain.linearRampToValueAtTime(0, now + 0.2);
                    osc.start(now);
                    osc.stop(now + 0.2);
                    break;
                    
                case 'error':
                    osc.type = 'sawtooth';
                    osc.frequency.setValueAtTime(130, now);
                    gain.gain.setValueAtTime(0.1, now);
                    gain.gain.linearRampToValueAtTime(0, now + 0.2);
                    osc.start(now);
                    osc.stop(now + 0.2);
                    break;
            }
        } catch (e) {
            console.warn("Sound playback error:", e);
        }
    }

