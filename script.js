    // Fill mode: Place number from bank into cell
    function handlePlaceNum(cellIdx, num) {
        const currentVal = board[cellIdx];
        
        // Update board
        board[cellIdx] = num;
        moveCount++;
        updateStatsUI();
        
        // Animate
        const cell = document.querySelector(`.grid-cell[data-index="${cellIdx}"]`);
        updateCellContent(cell, cellIdx);
        
        const tile = cell.querySelector('.tile-num');
        if (tile) tile.classList.add('placed-anim');
        
        renderNumberBank();
        calculateAndSetSums();
        playSound('place');
        
        checkWinCondition();
    }
    
    // Move / Swap number between two grid cells
    function handleMoveNum(fromIdx, toIdx) {
        if (clues[fromIdx] || clues[toIdx]) {
            playSound('error');
            return;
        }
        
        const temp = board[toIdx];
        board[toIdx] = board[fromIdx];
        board[fromIdx] = temp;
        
        moveCount++;
        updateStatsUI();
        
        // Update cells in DOM
        const cellFrom = document.querySelector(`.grid-cell[data-index="${fromIdx}"]`);
        const cellTo = document.querySelector(`.grid-cell[data-index="${toIdx}"]`);
        
        updateCellContent(cellFrom, fromIdx);
        updateCellContent(cellTo, toIdx);
        
        // Add animations
        const tileFrom = cellFrom.querySelector('.tile-num');
        const tileTo = cellTo.querySelector('.tile-num');
        if (tileFrom) tileFrom.classList.add('placed-anim');
        if (tileTo) tileTo.classList.add('placed-anim');
        
        if (gameMode === 'fill') {
            renderNumberBank();
        }
        
        calculateAndSetSums();
        playSound('swap');
        
        checkWinCondition();
    }

    /* ==========================================================================
       Math Verification (Sums computation & indicators updates)
       ========================================================================== */
    function calculateAndSetSums() {
        const magicSum = getMagicConstant(gridSize);
        
        // Initialize structures to hold sums
        const rowSums = Array(gridSize).fill(0);
        const colSums = Array(gridSize).fill(0);
        const rowCompletes = Array(gridSize).fill(true);
        const colCompletes = Array(gridSize).fill(true);
        
        let diag1Sum = 0;
        let diag2Sum = 0;
        let diag1Complete = true;
        let diag2Complete = true;
        
        // Calculate
        for (let r = 0; r < gridSize; r++) {
            for (let c = 0; c < gridSize; c++) {
                const idx = r * gridSize + c;
                const val = board[idx];
                
                if (val === 0) {
                    rowCompletes[r] = false;
                    colCompletes[c] = false;
                    if (r === c) diag1Complete = false;
                    if (r + c === gridSize - 1) diag2Complete = false;
                } else {
                    rowSums[r] += val;
                    colSums[c] += val;
                    if (r === c) diag1Sum += val;
                    if (r + c === gridSize - 1) diag2Sum += val;
                }
            }
        }
        
        // Update Row indicators
        for (let r = 0; r < gridSize; r++) {
            const indicator = document.getElementById(`sum-row-${r}`);
            if (!indicator) continue;
            
            indicator.innerText = rowSums[r];
            indicator.className = 'sum-indicator row-indicator';
            
            if (rowCompletes[r]) {
                if (rowSums[r] === magicSum) {
                    indicator.classList.add('correct');
                } else {
                    indicator.classList.add('incorrect');
                }
            } else if (rowSums[r] > 0) {
                indicator.classList.add('partial');
            }
        }
        
        // Update Col indicators
        for (let c = 0; c < gridSize; c++) {
            const indicator = document.getElementById(`sum-col-${c}`);
            if (!indicator) continue;
            
            indicator.innerText = colSums[c];
            indicator.className = 'sum-indicator col-indicator';
            
            if (colCompletes[c]) {
                if (colSums[c] === magicSum) {
                    indicator.classList.add('correct');
                } else {
                    indicator.classList.add('incorrect');
                }
            } else if (colSums[c] > 0) {
                indicator.classList.add('partial');
            }
        }
        
        // Update Diags indicators
        const diag1Indicator = document.getElementById('sum-diag-1');
        if (diag1Indicator) {
            diag1Indicator.querySelector('.sum-val').innerText = diag1Sum;
            diag1Indicator.className = 'diag-sum tl-br';
            
            if (diag1Complete) {
                if (diag1Sum === magicSum) {
                    diag1Indicator.classList.add('correct');
                } else {
                    diag1Indicator.classList.add('incorrect');
                }
            } else if (diag1Sum > 0) {
                diag1Indicator.classList.add('partial');
            }
        }
        
        const diag2Indicator = document.getElementById('sum-diag-2');
        if (diag2Indicator) {
            diag2Indicator.querySelector('.sum-val').innerText = diag2Sum;
            diag2Indicator.className = 'diag-sum tr-bl';
            
            if (diag2Complete) {
                if (diag2Sum === magicSum) {
                    diag2Indicator.classList.add('correct');
                } else {
                    diag2Indicator.classList.add('incorrect');
                }
            } else if (diag2Sum > 0) {
                diag2Indicator.classList.add('partial');
            }
        }
    }
    
    // Check if the board is successfully solved
    function checkWinCondition() {
        const magicSum = getMagicConstant(gridSize);
        const totalCells = gridSize * gridSize;
        
        // Check if all slots are filled
        if (board.some(x => x === 0)) return false;
        
        // Check Row Sums
        for (let r = 0; r < gridSize; r++) {
            let rowSum = 0;
            for (let c = 0; c < gridSize; c++) {
                rowSum += board[r * gridSize + c];
            }
            if (rowSum !== magicSum) return false;
        }
        
        // Check Col Sums
        for (let c = 0; c < gridSize; c++) {
            let colSum = 0;
            for (let r = 0; r < gridSize; r++) {
                colSum += board[r * gridSize + c];
            }
            if (colSum !== magicSum) return false;
        }
        
        // Check Diagonals
        let diag1Sum = 0;
        let diag2Sum = 0;
        for (let i = 0; i < gridSize; i++) {
            diag1Sum += board[i * gridSize + i];
            diag2Sum += board[i * gridSize + (gridSize - 1 - i)];
        }
        if (diag1Sum !== magicSum || diag2Sum !== magicSum) return false;
        
        // Check Unique numbers (1 to N^2)
        const numSet = new Set(board);
        if (numSet.size !== totalCells) return false;
        
        // If we reached here, victory!
        triggerVictory();
        return true;
    }
    
